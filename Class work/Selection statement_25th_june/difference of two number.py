'''To calculate the difference of two numbers'''
#input two numbers
num1 = float(input("Enter the first number:"))
num2 = float(input("Enter the second number:"))
#calculating the difference of two numbers
difference = num1 - num2
#displaying the difference of two numbers
print("The difference of two number is:", difference)

'''output:
Enter the first number: 10
Enter the second number: 5
The difference of two number is: 5.0'''